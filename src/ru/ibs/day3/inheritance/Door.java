package ru.ibs.day3.inheritance;

public class Door {
    private int width;
    private int height;
    public void setWidth(int desiredWidth) {
        width = desiredWidth;
    }
    public int getWidth() {
        return width;
    }
    public int getHeight() {
        return height;
    }
    public void setHeight(int height) {
        this.height = height;
    }
}
